    <script>
        tinymce.init({
            selector: 'textarea#my-editor', // Targets a textarea with the id "my-editor"
            height: 400,
            plugins: [
                'advlist', 'autolink', 'lists', 'link', 'image', 'charmap', 'preview',
                'anchor', 'searchreplace', 'visualblocks', 'code', 'fullscreen',
