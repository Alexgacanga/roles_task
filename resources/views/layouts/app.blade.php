            // 2. The Custom Upload Handler for Laravel
    images_upload_handler: (blobInfo, progress) => new Promise((resolve, reject) => {
        const formData = new FormData();
        formData.append('file', blobInfo.blob(), blobInfo.filename());

        // Fetch Laravel's CSRF token from the meta tag
        const csrfToken = document.querySelector('meta[name="csrf-token"]').getAttribute('content');

        fetch('/upload-image', { // Your Laravel route
            method: 'POST',
            headers: {
                'X-CSRF-TOKEN': csrfToken
            },
            body: formData
        })
        .then(response => {
            if (!response.ok) {
                throw new Error('HTTP Error: ' + response.status);
            }
            return response.json();
        })
        .then(json => {
            if (!json || typeof json.location != 'string') {
                reject('Invalid JSON: ' + JSON.stringify(json));
                return;
            }
            // TinyMCE expects a JSON response with a "location" key containing the URL
            resolve(json.location);
        })
        .catch(error => {
            reject('Upload failed: ' + error.message);
        });
    })
        });
    </script>


